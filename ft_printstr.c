#include "ft_printf.h"

int	ft_printstr(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i] != '\0')
	{
		n = n + ft_printchar(str[i]);
		i++;
	}
	return (n);
}
